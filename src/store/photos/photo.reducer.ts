const initialState: PhotoInterface.PhotoState = {
	listOfPhotos: [],
	loading: false,
	loadingLoadMore: false,
	total: 0,
	total_pages: 0,
	current_keyword: '',
	current_page: 1,
};

type Actions = { type: string; payload: any };

const contactReducers = (
	state = initialState,
	action: Actions,
): PhotoInterface.PhotoState => {
	const { type, payload } = action;
	console.log('type', type);
	switch (type) {
		case Dispatches.LOADING_PHOTO:
			return {
				...state,
				loading: payload,
			};
		case Dispatches.GET_PHOTO_BY_QUERY:
			return {
				...state,
				listOfPhotos: payload.results,
				total: payload.total,
				total_pages: payload.total_pages,
				current_keyword: payload.current_keyword,
				current_page: 1,
				loading: false,
			};
		case Dispatches.LOAD_MORE:
			return {
				...state,
				listOfPhotos: state.listOfPhotos.concat(payload.results),
				current_page: payload.page,
				loading: false,
			};
		default:
			return state;
	}
};

export default contactReducers;
